public class Extra3Clientes {
    private String nombre;
    private String apellido;
    private Long documento;
    private String mail;
    private String domicilio;
    private Integer telefono;

    
    // Constructores
    
    public Extra3Clientes() {
    }

    public Extra3Clientes(String nombre, String apellido, Long documento, String mail, String domicilio, Integer telefono) {
        this.nombre = nombre;
        this.apellido = apellido;
        this.documento = documento;
        this.mail = mail;
        this.domicilio = domicilio;
        this.telefono = telefono;
    }
    
    
}


